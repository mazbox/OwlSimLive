#pragma once
#include "StompBox.h"

class MutePatch: public Patch {
public:
	
	virtual void processAudio(AudioInputBuffer &input, AudioOutputBuffer &output) {
		float* y = output.getSamples();
		int size = input.getSize();
		memset(y, 0, size*sizeof(float));
	}
};

