#pragma once
#include "StompBox.h"

class OutBuffer: public AudioOutputBuffer {
public:
	OutBuffer() {
		size = 0;
	}
	
	int size;
	float *samples;
	
	void setSamples(int from, int length, float* data) {
		memcpy(&samples[from], data, length*sizeof(float));
	}
	
	void setSamples(float* data) {
		memcpy(samples, data, size*sizeof(float));
	}
	
	float* getSamples() {
		return samples;
	}
	
	int getSize() {
		return size;
	}
};