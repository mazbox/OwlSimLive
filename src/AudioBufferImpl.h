#pragma once
#include "StompBox.h"

class AudioBufferImpl: public AudioBuffer {
public:
	float *samples;
	int size;
	AudioBufferImpl() {
		size = 0;
	}
	void getSamples(int from, int length, float* data) {
		memcpy(data, &samples[from], length * sizeof(float));
	}
	
	float* getSamples(int channel) {
		return samples;
	}
	int getSize() {
		return size;
	}

	int getChannels() {
		return 1;
	}
};
